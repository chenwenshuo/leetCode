		int[] res = new int[nums.length-k+1];
		int[] tem = new int[k];
		System.arraycopy(nums,0,tem,0,k);
		Arrays.sort(tem);
		for (int i = 0; i < res.length; i++) {
			if(i!=0){
				int index = Arrays.binarySearch(tem,nums[i-1]);
				tem[index] = nums[i+k-1];
				Arrays.sort(tem);
			}
			if(tem[x-1]<0){
				res[i] = tem[x-1];
			}
		}
		return res;

