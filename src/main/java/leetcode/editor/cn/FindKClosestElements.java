class Solution {
    public List<Integer> findClosestElements(int[] arr, int k, int x) {
        int l=0,r=arr.length-1;
        while (l<r){
            if (r-l+1==k) break;
            if (Math.abs(arr[l]-x)<= Math.abs(arr[r]-x)){
                r--;
            }else {
                l++;
            }

        }
     List<Integer> list = new ArrayList<>();
        for (int i = l; i <=r; i++) {
           list.add(arr[i]);
            }

        return list;

    }
}
