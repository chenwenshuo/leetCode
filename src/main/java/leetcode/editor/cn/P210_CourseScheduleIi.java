    class Solution {

        public int[] findOrder(int numCourses, int[][] prerequisites) {
            Set<Integer>[] nums = new Set[numCourses];
            Arrays.setAll(nums, j -> new TreeSet<Integer>());
            int[] ans = new int[numCourses];
            int k = 0;

            int[] vst = new int[numCourses];
            Arrays.fill(vst, 0);
            for (int[] prerequisite : prerequisites) {
                if (prerequisite[0] == prerequisite[1]) {
                    return new int[]{};
                }
                vst[prerequisite[0]]++;
                nums[prerequisite[1]].add(prerequisite[0]);
            }

            Queue<Integer> queue = new LinkedList<>();

            for (int i = 0; i < nums.length; i++) {
                if (vst[i] == 0) {
                    queue.offer(i);
                }
            }
            if (queue.isEmpty()) {
                return new int[]{};
            }
            while (!queue.isEmpty()) {
                int size = queue.size();
                for (int i = 0; i < size; i++) {
                    Integer poll = queue.poll();
                    ans[k] = poll;
                    k++;
                    numCourses--;
                    if (nums[poll].size() > 0) {
                        for (int o : nums[poll]) {
                            if (--vst[o] == 0) {
                                queue.offer(o);
                            }
                        }
                    }
                }
            }

            return numCourses == 0 ? ans : new int[]{};

        }
    }
