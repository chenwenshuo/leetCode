    class Solution {
        public int searchInsert(int[] nums, int target) {

            int ans = 0;
            int l = 0, r = nums.length - 1;
            //nums = [1,3,5,6], target = 5
            while (l <= r) {
                int mid=l+r>>1;
                if (target>nums[mid]){
                    ans=mid+1;
                    l=mid+1;
                }else {
                    r=mid-1;
                }
            }

            return ans;
        }
    }
