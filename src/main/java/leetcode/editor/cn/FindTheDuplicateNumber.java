class Solution {
    public int findDuplicate(int[] nums) {
        int n = nums.length;
        int l = 1, r = n - 1, ans = -1;
        while (l <= r) {
            int mid = (l + r) >> 1;

            if (check(nums,mid)){
                r=mid-1;
                ans=mid;
            }else {
                l=mid+1;

            }
        }
        return ans;

    }

    private boolean check(int[] nums, int mid) {
        int tem=0;
        for (int num : nums) {
            if (num<=mid) tem++;
        }
        return tem>mid;
    }
}


    public int findDuplicate1(int[] nums) {
        int n = nums.length;
        int l = 1, r = n - 1, ans = -1;
        while (l <= r) {
            int mid = (l + r) >> 1;
            int cnt = 0;
            for (int i = 0; i < n; ++i) {
                if (nums[i] <= mid) {
                    cnt++;
                }
            }
            if (cnt <= mid) {
                l = mid + 1;
            } else {
                r = mid - 1;
                ans = mid;
            }
        }
        return ans;
    }

