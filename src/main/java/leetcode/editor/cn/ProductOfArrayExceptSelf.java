class Solution {
    public int[] productExceptSelf(int[] nums) {
        int length = nums.length;
        int[] ans=new int[length];
        int tem=1;
        int flag=0;
        for (int num : nums) {
            if (num==0) flag++;
            else tem*=num;
        }

        for (int i = 0; i < nums.length; i++) {
            if (nums[i]!=0){
                if (flag>0) ans[i]=0;
                else ans[i]=tem/nums[i];
            }else {
                if ((flag-1)==0) ans[i]=tem;
                else  ans[i]=0;
            }
        }
return ans;
    }
}
